package com.itheima;
//定义不同的数据类型并打印

public class TestDemo01 {
    public static void main(String[] args) {
        int b = 20;
        System.out.println(b);
        float c = 1.2F;
        System.out.println(c);
        char d = 'A';
        System.out.println(d);
        long e = 1234567L;
        System.out.println(e);
    }
}